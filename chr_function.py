print("Without chr function : ");
for letter in range(65 , 91):
    print(letter,end=",");

print("");
print("");

print("With chr function : ");
for letter2 in range(65 , 91):
    print(chr(letter2),end=",");

print("");
print("");

print("With chr function : ");
for letter3 in range(ord('A'),ord('Z')+1):
    print(chr(letter3),end=",");

    
